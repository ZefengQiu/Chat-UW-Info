import Foundation


extension Dictionary {
  /**
  Checks if a key exists in the dictionary.
  
  - parameter key: Key to check
  - returns: true if the key exists
  */
  func has (key: Key) -> Bool {
    return indexForKey(key) != nil
  }
  
}